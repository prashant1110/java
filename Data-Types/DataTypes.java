class DataTypes {
    public static void main(String[] args) {

        /**
         * 1.Primitive Data type
         * boolean,character
         * byte,short,int,long
         * float,double
         */
        // boolean
        boolean bool = true;// 1 bit

        // character
        char letter = 'a';// 2 byte

        // byte
        byte num = 23;// 1 byte

        // short
        short num5 = 4555;// 2 byte

        // integer
        int num1 = 45555;// 4 byte

        // long
        long num6 = 446946946;// 8 byte

        // float
        float num3 = 45.45f;// 4 byte

        // double
        double num4 = 45.26949494;// 8 byte

        /*
         * 2.Non-primitive data type
         * class,interface,object,string,Array etc
         */
        // string
        String name = "Prashant singh";

        // Array

        System.out.println("Boolean" + bool);
        System.out.println("Byte" + num);
        System.out.println("Short" + num5);
        System.out.println("Integer" + num1);
        System.out.println("Long" + num6);
        System.out.println("Float" + num3);
        System.out.println("Double" + num4);
        System.out.println("String" + name);

    }
}